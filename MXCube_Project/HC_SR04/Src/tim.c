double  get_distance()
	{
    //0.1s计数器触发事件
    double  times;    //时间
    double  distance;  //距离
    double     count;     //计数次数
	
    __HAL_TIM_SET_COUNTER(&htim7,0);     //定时器复位
    __HAL_TIM_SET_AUTORELOAD(&htim7,4000-1);
        

    HAL_GPIO_WritePin(GPIOB,GPIO_PIN_4,1);  //开启超声波
    HAL_Delay(100);
    HAL_GPIO_WritePin(GPIOB,GPIO_PIN_4,0);
    
    while(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_5)==0) //等待启动信号
		{}
        HAL_TIM_Base_Start_IT(&htim7);           //启动定时器
     
    while(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_5)==1) //等待停止信号
    {}
        HAL_TIM_Base_Stop(&htim7);               //停止定时器
     

        count  = __HAL_TIM_GET_COUNTER(&htim7);  //获取计数次数
			
				
        printf("\n计数器计数的次数:%lf\n",count); //打印
        
        times = count*10;                       //得出时间，秒
        
        distance = times * 340 / 10000.0 / 2.0;   //厘米
        
    return distance;
}
